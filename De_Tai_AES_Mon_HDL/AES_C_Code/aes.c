static uint8_t getSBoxValue(uint8_t num) {
    return sbox[num];
}

static void KeyExpansion(void) {
    uint32_t temp;
    uint32_t i;

    for (i = 0; i < Nk; ++i) {
        RoundKey[i] = Key[i];
    }

    for (i = Nk; i < Nb * (Nr + 1); ++i) {
        temp = RoundKey[i - 1];
        if (i % Nk == 0) {
            temp = (getSBoxValue((temp >> 16) & 0xff) << 24) |
                   (getSBoxValue((temp >> 8) & 0xff) << 16) |
                   (getSBoxValue(temp & 0xff) << 8) |
                   (getSBoxValue((temp >> 24) & 0xff));
            temp ^= ((uint32_t)Rcon[i / Nk] << 24);
        }
        RoundKey[i] = RoundKey[i - Nk] ^ temp;
    }
}

static void AddRoundKey(uint8_t round) {
    for (uint8_t i = 0; i < 4; ++i) {
        (*state)[i] ^= RoundKey[round * Nb + i];
    }
}

static void SubBytes(void) {
    for (uint8_t i = 0; i < 4; ++i) {
        uint32_t word = (*state)[i];
        (*state)[i] = (getSBoxValue((word >> 24) & 0xFF) << 24) |
                      (getSBoxValue((word >> 16) & 0xFF) << 16) |
                      (getSBoxValue((word >> 8) & 0xFF) << 8) |
                      (getSBoxValue(word & 0xFF));
    }
}

