static uint8_t xtime(uint8_t x) {
    return ((x << 1) ^ (((x >> 7) & 1) * 0x1b));
}

static void MixColumns(void) {
    uint8_t a[4][4];
    for (int i = 0; i < 4; ++i) {
        uint32_t col = (*state)[i];
        a[i][0] = col >> 24;
        a[i][1] = col >> 16;
        a[i][2] = col >> 8;
        a[i][3] = col;
    }
    for (int i = 0; i < 4; ++i) {
        uint8_t t = a[i][0] ^ a[i][1] ^ a[i][2] ^ a[i][3];
        uint8_t u = a[i][0];
        a[i][0] ^= t ^ xtime(a[i][0] ^ a[i][1]);
        a[i][1] ^= t ^ xtime(a[i][1] ^ a[i][2]);
        a[i][2] ^= t ^ xtime(a[i][2] ^ a[i][3]);
        a[i][3] ^= t ^ xtime(a[i][3] ^ u);
    }
    for (int i = 0; i < 4; ++i) {
        (*state)[i] = ((uint32_t)a[i][0] << 24) |
                      ((uint32_t)a[i][1] << 16) |
                      ((uint32_t)a[i][2] << 8) |
                      ((uint32_t)a[i][3]);
    }
}

static void Cipher(void) {
    for (uint8_t round = 0; round <= 10; ++round) {
		if(round ==0){
			AddRoundKey(0);
		}
		else if(round <10) {
			SubBytes();
			ShiftRows(round);
			MixColumns();
			AddRoundKey(round);
		}
		else {
			SubBytes();
			ShiftRows(round);
			AddRoundKey(10);
		}
    }

}

static void BlockCopy(uint32_t* output, uint32_t* input) {
    for (int i = 0; i < KEYLEN / 4; ++i) {
        output[i] = input[i];
    }
}

void AES128_encrypt(uint32_t* input, const uint32_t* key, uint32_t* output) {
    BlockCopy(output, input);
    state = (state_t*)output;
    Key = key;
    KeyExpansion();
    Cipher();
}

